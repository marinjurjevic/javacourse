package hr.fer.zemris.java.custom.scripting.exec;

/**
 * Operation type models 4 basic operations that can be done between
 * <tt>ValueWrapper</tt> objects.
 * 
 * @author Marin Jurjevic
 *
 */
public enum OperationType {
	/**
	 * increment operation
	 */
	INC,

	/**
	 * decrement operation
	 */
	DEC,

	/**
	 * multiplication
	 */
	MUL,

	/**
	 * division
	 */
	DIV
}
